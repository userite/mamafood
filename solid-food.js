// Инициализиране при зареждане на страницата
document.addEventListener('DOMContentLoaded', () => {
    renderSolidFoodInfo();
    
    // Добавяне на слушател за таб бутона
    const solidFoodTab = document.getElementById('solidFoodTab');
    if (solidFoodTab) {
        solidFoodTab.addEventListener('click', () => {
            // Скриване на всички табове
            document.querySelectorAll('.tab-content').forEach(tab => {
                tab.style.display = 'none';
            });
            
            // Показване на таба за твърди храни
            const solidFoodTabContent = document.getElementById('solid-food-tab');
            if (solidFoodTabContent) {
                solidFoodTabContent.style.display = 'block';
            }
            
            // Активиране на бутона
            document.querySelectorAll('.tab-button').forEach(button => {
                button.classList.remove('active');
            });
            solidFoodTab.classList.add('active');
        });
    }
});